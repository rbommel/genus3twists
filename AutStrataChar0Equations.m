function GeometricAutomorphismGroup(C)
	D := DixmierOhnoInvariants(Equation(C));
	if IsInStratumG168(D) then
		return "G168", 168;
	elif IsInStratumG96(D) then
		return "G96", 96;
	elif IsInStratumG48(D) then
		return "G48", 48;
	elif IsInStratumS4(D) then
		return "S4", 24;
	elif IsInStratumG16(D) then
		return "G16", 16;
	elif IsInStratumC9(D) then
		return "C9", 9;
	elif IsInStratumD8(D) then
		return "D8", 8;
	elif IsInStratumC6(D) then
		return "C6", 6;
	elif IsInStratumS3(D) then
		return "S3", 6;
	elif IsInStratumD4(D) then
		return "D4", 4;
	elif IsInStratumC3(D) then
		return "C3", 3;
	elif #AutomorphismsOfTernaryQuartic(Equation(C)) eq 2 then
		return "C2", 2;
	else
		return "C1", 1;		
	end if;
end function;


